// Simple Array Concept

#include <stdio.h>

int main()
{
    int marks[4];
    marks[0] = 60;
    marks[1] = 74;
    marks[2] = 87;
    marks[3] = 92;
    printf("Marks Of Student-01 is : %d\n", marks[0]);   
    printf("Marks Of Student-02 is : %d\n", marks[1]);   
    printf("Marks Of Student-03 is : %d\n", marks[2]);   
    printf("Marks Of Student-04 is : %d\n", marks[3]);

    return 0;
}